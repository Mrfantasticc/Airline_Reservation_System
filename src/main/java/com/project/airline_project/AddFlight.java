    public void AutoId(){
        try {
//            starting connection with database
            Connection conn;
            PreparedStatement pre;
//            setting driver to establish connection between frontend(UI) and backend(sql)
            Class.forName("com.mysql.jdbc.Driver");
//            geting connected with database and doing database authencation with username and password of database
            conn=DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root","Ajaych970@");
//            fireing sql query
            pre=conn.prepareStatement("Select MAX(FlightId) from flight");
            
            ResultSet rs=pre.executeQuery();
//            initalizing result set
            rs.next();
            
//            checking that customerId has maximum value or not
//            if not thent then set it as CS001
//            otherwise increase iths value by 1
            if(rs.getString("MAX(FlightId)")==null){
                flightid.setText("FL001");
            }else{
                long id=Long.parseLong(rs.getString("MAX(FlightId)").substring(2, rs.getString("MAX(FlightId)").length()));
                id++;
//                taking 3 digits from last to update customer id by using string format
                flightid.setText("FL"+String.format("%03d", id));
                
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

